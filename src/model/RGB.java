package model;

public class RGB
{
	private double r;
	private double g;
	private double b;
	
	public RGB()
	{
		setR(0);
		setG(0);
		setB(0);
	}

	public double getR() 
	{
		return r;
	}

	public void setR(double r) 
	{
		this.r = r;
	}

	public double getG() 
	{
		return g;
	}

	public void setG(double g) 
	{
		this.g = g;
	}

	public double getB() 
	{
		return b;
	}

	public void setB(double b) 
	{
		this.b = b;
	}
}
